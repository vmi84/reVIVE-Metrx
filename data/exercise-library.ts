/** Get exercise by ID */
export function getExercise(id: string): ExerciseDemo | undefined {
  return EXERCISE_LIBRARY.find(e => e.id === id);
}

/** Get exercises for a specific protocol slug */
export function getExercisesForProtocol(slug: string): ExerciseDemo[] {
  return EXERCISE_LIBRARY.filter(e => e.protocolSlugs.includes(slug));
}

/** Get exercises for a specific modality key */
export function getExercisesForModality(key: string): ExerciseDemo[] {
  return EXERCISE_LIBRARY.filter(e => e.modalityKeys.includes(key));
}

/** Get exercises by category */
export function getExercisesByCategory(category: ExerciseCategory): ExerciseDemo[] {
  return EXERCISE_LIBRARY.filter(e => e.category === category);
}

/** Get all lymphatic-supporting exercises */
export function getLymphaticExercises(): ExerciseDemo[] {
  return EXERCISE_LIBRARY.filter(e => e.supportsLymphatic);
}

/** Search exercises by name or target area */
export function searchExercises(query: string): ExerciseDemo[] {
  const q = query.toLowerCase();
  return EXERCISE_LIBRARY.filter(e =>
    e.name.toLowerCase().includes(q) ||
    e.targetAreas.some(t => t.includes(q)) ||
    e.category.includes(q),
  );
}
